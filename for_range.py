# izvada skaitlus no 1 lidz 6 (6 neieskaitot)

for x in range(1, 6):
    print(x, end='\t')

# izvada skaitlus no 2 lidz 11 (11 neieskaitot) ar soli 2

for x in range(2, 11, 2):
    print(x, end='\t')
# izvada skaitlus no 10 lidz 0 (0 neieskaitot) ar soli -1
from time import sleep
for x in range(10, 0, -1):
    print(x, end='\t')
    sleep(1)
